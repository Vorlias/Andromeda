namespace Vorlias2D.System
{
    public enum MouseConstraintType
    {
        /// <summary>
        /// The mouse behaves normally
        /// </summary>
        Normal,

        /// <summary>
        /// The mouse is constrained to the window
        /// </summary>
        ConstrainedWindow,

        /// <summary>
        /// The mouse is constrained to the center
        /// </summary>
        ConstrainedCenter,
    }
}
